#include "libft.h"

char	*ft_strnstr(const char *str, const char *substr, size_t len)
{
	size_t	i;
	size_t	j;

	if (*substr == '\0')
		return ((char *)str);
	i = 0;
	while (str[i] && i < len)
	{
		j = 0;
		while (str[i + j] == substr[j] && i + j < len)
		{
			j++;
			if (substr[j] == '\0')
				return ((char *)(str + i));
		}
		i++;
	}
	return (0);
}
